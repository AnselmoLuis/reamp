
public class Main{
    private Long temp;
    private Long pressure;
    private Long humidity;
    private Long temp_min;
    private Long temp_max;

    /**
     * @return Long return the temp
     */
    public Long getTemp() {
        return temp;
    }

    /**
     * @param temp the temp to set
     */
    public void setTemp(Long temp) {
        this.temp = temp;
    }

    /**
     * @return Long return the pressure
     */
    public Long getPressure() {
        return pressure;
    }

    /**
     * @param pressure the pressure to set
     */
    public void setPressure(Long pressure) {
        this.pressure = pressure;
    }

    /**
     * @return Long return the humidity
     */
    public Long getHumidity() {
        return humidity;
    }

    /**
     * @param humidity the humidity to set
     */
    public void setHumidity(Long humidity) {
        this.humidity = humidity;
    }

    /**
     * @return Long return the temp_min
     */
    public Long getTemp_min() {
        return temp_min;
    }

    /**
     * @param temp_min the temp_min to set
     */
    public void setTemp_min(Long temp_min) {
        this.temp_min = temp_min;
    }

    /**
     * @return Long return the temp_max
     */
    public Long getTemp_max() {
        return temp_max;
    }

    /**
     * @param temp_max the temp_max to set
     */
    public void setTemp_max(Long temp_max) {
        this.temp_max = temp_max;
    }

}