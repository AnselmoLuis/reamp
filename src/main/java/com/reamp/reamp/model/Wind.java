
public class Wind{
    private Long speed;
    private Long deg;

    /**
     * @return Long return the speed
     */
    public Long getSpeed() {
        return speed;
    }

    /**
     * @param speed the speed to set
     */
    public void setSpeed(Long speed) {
        this.speed = speed;
    }

    /**
     * @return Long return the deg
     */
    public Long getDeg() {
        return deg;
    }

    /**
     * @param deg the deg to set
     */
    public void setDeg(Long deg) {
        this.deg = deg;
    }

}