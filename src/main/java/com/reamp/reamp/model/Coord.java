
public class Coord{
    private Long lon;
    private Long lat;

    /**
     * @return Long return the lon
     */
    public Long getLon() {
        return lon;
    }

    /**
     * @param lon the lon to set
     */
    public void setLon(Long lon) {
        this.lon = lon;
    }

    /**
     * @return Long return the lat
     */
    public Long getLat() {
        return lat;
    }

    /**
     * @param lat the lat to set
     */
    public void setLat(Long lat) {
        this.lat = lat;
    }

}
